# Test 1: evaluate_on_holdout
# ---------------------------------------------------------------------------

def test_evaluate_on_holdout_shape_and_keys():
    from sklearn.pipeline import Pipeline
    from sklearn.preprocessing import StandardScaler
    from sklearn.feature_selection import VarianceThreshold
    from sklearn.svm import SVC

    X, y, authors, gids = make_synthetic_corpus()

    # Hold out 4 ids (1 per of 4 genres). Train on the remaining 44.
    holdout_ids = [gids[0], gids[6], gids[12], gids[18]]
    holdout_mask = np.array([g in holdout_ids for g in gids])
    pipe = Pipeline([
        ("scaler", StandardScaler()),
        ("vt", VarianceThreshold(threshold=1e-4)),
        ("svm", SVC(kernel="rbf", C=10.0, gamma="scale",
                    class_weight="balanced", random_state=42)),
    ])
    pipe.fit(X[~holdout_mask], y[~holdout_mask])

    result = _validate.evaluate_on_holdout(pipe, X, y, gids, holdout_ids)

    for key in ("macro_f1", "per_genre_f1", "accuracy", "confusion_matrix",
                "n_in_comparison", "in_comparison_ids", "out_of_comparison_ids",
                "predictions"):
        assert key in result, f"missing key: {key}"

    assert result["n_in_comparison"] == 4
    assert isinstance(result["macro_f1"], float)
    assert isinstance(result["per_genre_f1"], dict)
    assert isinstance(result["confusion_matrix"], list)
    assert all(isinstance(p, dict) for p in result["predictions"])
    assert len(result["predictions"]) == 4


def test_evaluate_on_holdout_out_of_comparison():
    from sklearn.pipeline import Pipeline
    from sklearn.preprocessing import StandardScaler
    from sklearn.svm import SVC

    X, y, authors, gids = make_synthetic_corpus(n_books=16, n_features=10, n_genres=8)
    pipe = Pipeline([
        ("scaler", StandardScaler()),
        ("svm", SVC(kernel="rbf", C=10.0, gamma="scale",
                    class_weight="balanced", random_state=42)),
    ])
    pipe.fit(X, y)

    # 2 in-corpus + 2 out-of-corpus
    holdout_ids = [gids[0], gids[5], 999999, 888888]
    result = _validate.evaluate_on_holdout(pipe, X, y, gids, holdout_ids)
    assert result["n_in_comparison"] == 2
    assert set(result["out_of_comparison_ids"]) == {999999, 888888}


