def test_per_author_held_out_filters_single_book_authors():
    """Authors with only 1 book in the corpus must NOT participate."""
    X, y, authors, gids = make_synthetic_corpus()
    # Add a single-book ghost author
    X_aug = np.vstack([X, X[:1] + 0.01])
    y_aug = np.append(y, y[0])
    authors_aug = np.append(authors, "ghost_solo_author")
    result = _validate.per_author_held_out_smoke_test(X_aug, y_aug, authors_aug, loocv_acc=0.8)
    assert "ghost_solo_author" not in result["per_author_accuracy"]


