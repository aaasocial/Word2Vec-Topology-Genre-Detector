def make_synthetic_corpus(n_books=48, n_features=20, n_genres=8, seed=0):
    """Build a small, well-separated synthetic feature matrix.

    Each genre has its own latent centroid; features are centroid + small noise.
    This guarantees the SVM finds real signal (so per-author smoke tests have a
    non-trivial loocv_acc to compute a gap from).
    """
    rng = np.random.default_rng(seed)
    books_per_genre = n_books // n_genres
    centroids = rng.normal(scale=5.0, size=(n_genres, n_features))

    X = np.zeros((n_books, n_features), dtype=np.float64)
    y = np.zeros(n_books, dtype=np.int64)
    authors = np.empty(n_books, dtype=object)

    gutenberg_ids = []
    for g in range(n_genres):
        for b in range(books_per_genre):
            i = g * books_per_genre + b
            X[i] = centroids[g] + rng.normal(scale=0.5, size=n_features)
            y[i] = g
            # Assign each genre 3 distinct authors (2 books each on avg)
            authors[i] = f"author_{g}_{b // 2}"
            gutenberg_ids.append(1000 + i)

    return X, y, authors, gutenberg_ids


