# Test 5: all four functions deterministic (per task spec)
# ---------------------------------------------------------------------------

def test_all_four_functions_deterministic():
    """Re-running with identical inputs returns identical dicts."""
    from sklearn.pipeline import Pipeline
    from sklearn.preprocessing import StandardScaler
    from sklearn.feature_selection import VarianceThreshold
    from sklearn.svm import SVC

    X, y, authors, gids = make_synthetic_corpus()

    pipe = Pipeline([
        ("scaler", StandardScaler()),
        ("vt", VarianceThreshold(threshold=1e-4)),
        ("svm", SVC(kernel="rbf", C=10.0, gamma="scale",
                    class_weight="balanced", random_state=42)),
    ])
    pipe.fit(X, y)

    holdout_ids = [gids[0], gids[6]]
    a1 = _validate.evaluate_on_holdout(pipe, X, y, gids, holdout_ids)
    a2 = _validate.evaluate_on_holdout(pipe, X, y, gids, holdout_ids)
    assert a1["macro_f1"] == a2["macro_f1"]
    assert a1["per_genre_f1"] == a2["per_genre_f1"]

    b1 = _validate.cross_validate_grouped(X, y, authors, n_splits=4)
    b2 = _validate.cross_validate_grouped(X, y, authors, n_splits=4)
    assert b1 == b2

    c1 = _validate.per_author_held_out_smoke_test(X, y, authors, loocv_acc=0.8)
    c2 = _validate.per_author_held_out_smoke_test(X, y, authors, loocv_acc=0.8)
    assert c1["per_author_accuracy"] == c2["per_author_accuracy"]
    assert c1["mean_gap_pp"] == c2["mean_gap_pp"]

    d1 = _validate.permutation_null_test(X, y, n_permutations=15, random_state=42)
    d2 = _validate.permutation_null_test(X, y, n_permutations=15, random_state=42)
    assert d1["p_value"] == d2["p_value"]
    assert d1["observed_macro_f1"] == d2["observed_macro_f1"]


